# Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês.
# Calcule e mostre o total do seu salário no referido mês.

preco_hora = float(input('Quanto você ganha por hora? \n'))
horas = float(input('Quantas horas você trabalhou nesse mês? \n'))
print(f'O seu salário esse mês foi de R${preco_hora * horas}')
